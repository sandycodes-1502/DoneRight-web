import {
    getFirestore,
    collection,
    doc,
    setDoc,
    updateDoc,
    deleteDoc,
    onSnapshot,
    query,
    orderBy
} from "https://www.gstatic.com/firebasejs/10.8.1/firebase-firestore.js";
import { db } from '/js/auth.js';

// Valid task statuses (matching Android app and Firestore)
export const TASK_STATUS = {
    TODO: 'TODO',
    IN_PROGRESS: 'IN_PROGRESS',
    DONE: 'DONE'
};

// Status cycle order for clicking the badge
const STATUS_CYCLE = [TASK_STATUS.TODO, TASK_STATUS.IN_PROGRESS, TASK_STATUS.DONE];

/**
 * Create a new task under the authenticated user's tasks subcollection.
 * ID is a UUID generated by the browser (matches Android's UUID format).
 */
export async function createTask(uid, { title, description = '', status = TASK_STATUS.TODO }) {
    const taskId = crypto.randomUUID();
    const now = Date.now();
    const taskRef = doc(db, 'users', uid, 'tasks', taskId);
    await setDoc(taskRef, {
        id: taskId,
        title: title.trim(),
        description: description.trim(),
        status,
        createdAt: now,
        updatedAt: now
    });
    return taskId;
}

/**
 * Subscribe to real-time task updates for a user.
 * Returns the unsubscribe function.
 */
export function loadTasks(uid, callback) {
    const tasksRef = collection(db, 'users', uid, 'tasks');
    const q = query(tasksRef, orderBy('createdAt', 'desc'));
    return onSnapshot(q, (snapshot) => {
        const tasks = snapshot.docs.map(d => d.data());
        callback(tasks);
    });
}

/**
 * Update task status and bump updatedAt.
 */
export async function updateTaskStatus(uid, taskId, newStatus) {
    const taskRef = doc(db, 'users', uid, 'tasks', taskId);
    await updateDoc(taskRef, {
        status: newStatus,
        updatedAt: Date.now()
    });
}

/**
 * Update task title and/or description and bump updatedAt.
 */
export async function updateTask(uid, taskId, { title, description }) {
    const taskRef = doc(db, 'users', uid, 'tasks', taskId);
    const updates = { updatedAt: Date.now() };
    if (title !== undefined) updates.title = title.trim();
    if (description !== undefined) updates.description = description.trim();
    await updateDoc(taskRef, updates);
}

/**
 * Permanently delete a task document.
 */
export async function deleteTask(uid, taskId) {
    const taskRef = doc(db, 'users', uid, 'tasks', taskId);
    await deleteDoc(taskRef);
}

/**
 * Returns the next status in the cycle: TODO → IN_PROGRESS → DONE → TODO
 */
export function nextStatus(currentStatus) {
    const idx = STATUS_CYCLE.indexOf(currentStatus);
    return STATUS_CYCLE[(idx + 1) % STATUS_CYCLE.length];
}
